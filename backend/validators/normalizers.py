def normalize_category(raw: str, entry_type: str) -> str:
    """将 LLM 输出的原始分类名归一化到标准分类。

    Args:
        raw: LLM 输出的原始 category 值
        entry_type: "expense" | "income" | "schedule"

    Returns:
        标准化的分类名。如果无法匹配，返回原值或"其他"。
    """
    if not raw:
        return "其他"

    normalized = raw.strip()

    if entry_type == "expense":
        # 先查映射表
        if normalized in EXPENSE_NORMALIZE:
            return EXPENSE_NORMALIZE[normalized]
        # 再检查是否已经是标准分类
        standard = {"餐饮", "交通", "购物", "娱乐", "居住", "通讯", "医疗", "教育",
                    "服饰", "饮品", "日用", "其他"}
        if normalized in standard:
            return normalized
        # 无法匹配，返回"其他"
        return "其他"

    elif entry_type == "income":
        if normalized in INCOME_NORMALIZE:
            return INCOME_NORMALIZE[normalized]
        standard = {"工资", "奖金", "投资", "兼职", "红包", "退款", "报销", "其他"}
        if normalized in standard:
            return normalized
        return "其他"

    elif entry_type == "schedule":
        if normalized in SCHEDULE_NORMALIZE:
            return SCHEDULE_NORMALIZE[normalized]
        standard = {"工作", "学习", "聚会", "生日", "会议", "医疗", "出行", "其他"}
        if normalized in standard:
            return normalized
        return "其他"

    return normalized if normalized else "其他"


def normalize_expense_item(item: dict) -> dict:
    """归一化单条支出记录."""
    if item.get("category"):
        item["category"] = normalize_category(item["category"], "expense")
    return item


def normalize_income_item(item: dict) -> dict:
    """归一化单条收入记录."""
    if item.get("category"):
        item["category"] = normalize_category(item["category"], "income")
    return item


def normalize_schedule_item(item: dict) -> dict:
    """归一化单条日程记录."""
    if item.get("category"):
        item["category"] = normalize_category(item["category"], "schedule")
    return item
